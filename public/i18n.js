  const CACHE_VERSION = "v3";
  const runtimeCache = new Map();

  // Load all cached languages from localStorage on startup
  // Per-language keys: "netra-i18n-v3-Hindi", etc.
  function _loadLanguageCache(language) {
    try {
      const key = `netra-i18n-${CACHE_VERSION}-${language}`;
      const saved = JSON.parse(localStorage.getItem(key) || "{}");
      Object.entries(saved).forEach(([orig, trans]) => {
        if (typeof trans === "string") {
          runtimeCache.set(`${language}:${orig}`, trans);
          if (!contentPacks[language]) contentPacks[language] = {};
          contentPacks[language][orig] = trans;
        }
      });
      try { console.debug && console.debug(`[i18n] _loadLanguageCache(${language}) loaded ${Object.keys(saved).length} entries`); } catch (e) {}
    } catch {}
  }

  // Load a few common languages eagerly; rest load on demand
  ["Hindi","Marathi","Tamil","Telugu","Bengali","Gujarati","Kannada","Malayalam","Punjabi","Urdu"].forEach(_loadLanguageCache);

  function _ensureLanguageLoaded(language) {
    if (!language || language === "English") return;
    // Check if we've already loaded this language (any key would be present)
    const testKey = `netra-i18n-${CACHE_VERSION}-${language}-loaded`;
    if (sessionStorage.getItem(testKey)) return;
    sessionStorage.setItem(testKey, "1");
    _loadLanguageCache(language);
  }

  function packFor(language) {
    const aliasPack = packs[aliasSource[language]] || {};
    const exactPack = packs[language] || {};
    return { ...base, ...aliasPack, ...exactPack };
  }

  function format(text, params = {}) {
    return text.replace(/\{(\w+)\}/g, (_, key) => params[key] ?? "");
  }

  function t(language, key, params = {}) {
    _ensureLanguageLoaded(language);
    const baseValue = base[key] || key;
    const packValue = packFor(language)[key];
    const cached = runtimeCache.get(`${language}:${baseValue}`);
    return format(cached || packValue || baseValue, params);
  }

  function contentText(language, text) {
    if (!text) return text;
    _ensureLanguageLoaded(language);
    // Check runtime cache first
    const cacheKey = `${language}:${text}`;
    if (runtimeCache.has(cacheKey)) {
      const r = runtimeCache.get(cacheKey);
      try { console.debug && console.debug(`[i18n] contentText cache ${language}:${text} -> ${r}`); } catch(e){}
      return r;
    }

    // Check static content packs
    const langPack = contentPacks[language];
    if (langPack && langPack[text]) {
      try { console.debug && console.debug(`[i18n] contentText pack ${language}:${text} -> ${langPack[text]}`); } catch(e){}
      return langPack[text];
    }

    const aliasPack = contentPacks[aliasSource[language]];
    if (aliasPack && aliasPack[text]) {
      try { console.debug && console.debug(`[i18n] contentText alias ${language}(${aliasSource[language]}):${text} -> ${aliasPack[text]}`); } catch(e){}
      return aliasPack[text];
    }

    try { console.debug && console.debug(`[i18n] contentText miss ${language}: ${text}`); } catch(e){}
    return text; // Return original, caller may queue async translation
  }

  function cacheTranslation(language, original, translated) {
    runtimeCache.set(`${language}:${original}`, translated);
    if (!contentPacks[language]) contentPacks[language] = {};
    contentPacks[language][original] = translated;
    // Write to per-language localStorage key for fast startup next session
    try {
      const lsKey = `netra-i18n-${CACHE_VERSION}-${language}`;
      const saved = JSON.parse(localStorage.getItem(lsKey) || "{}");
      saved[original] = translated;
      localStorage.setItem(lsKey, JSON.stringify(saved));
      try { console.debug && console.debug(`[i18n] cacheTranslation ${language}: "${original}" -> "${translated}"`); } catch(e){}
    } catch {}
  }

  function hasCached(language, text) {
    if (language === "English") return true;
    _ensureLanguageLoaded(language);
    const cacheKey = `${language}:${text}`;
    if (runtimeCache.has(cacheKey)) return true;
    const staticKey = Object.keys(base).find(key => base[key] === text);
    if (staticKey && packs[language]?.[staticKey] && packs[language][staticKey] !== text) return true;
    const langPack = contentPacks[language] || (language === "Indian Sign Language" ? contentPacks[aliasSource[language]] : null);
    const ok = !!(langPack && langPack[text]);
    try { console.debug && console.debug(`[i18n] hasCached ${language}:${text} -> ${ok}`); } catch(e){}
    return ok;
  }

  function baseText(key) {
    return base[key] || key;
  }

  function staticTexts() {
    return [...new Set(Object.values(base).filter(value => typeof value === "string" && value.length > 1))];
  }

  return { t, contentText, cacheTranslation, hasCached, baseText, staticTexts };
